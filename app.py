@app.route('/venues')
def venues():
  data = []
  venue_list = Venue.query.distinct('state', 'city').all()
  for venue in venue_list:
      venue_data = {
          "city": venue.city,
          "state": venue.state,
          "venues": Venue.query.filter_by(city=venue.city).all()
      }
      data.append(venue_data)
  return render_template('pages/venues.html', areas=data)

